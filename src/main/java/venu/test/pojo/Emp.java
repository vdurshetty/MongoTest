package venu.test.pojo;

public class Emp {
	
	int empid;
	String name;
	/**
	 * @param empid
	 * @param name
	 */
	public Emp(int empid, String name) {
		super();
		this.empid = empid;
		this.name = name;
	}
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
